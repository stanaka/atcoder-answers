package main

import (
	"testing"

	"github.com/stanaka/atcoder-answers/utils"
)

type testCase struct {
	input  string
	expect string
}

func TestAnswer(t *testing.T) {
	testCases := []testCase{
		{`3 3 10
1 2 20
2 3 30
1 3 45`, `35`},
		{`2 2 10
1 2 100
2 2 100`, `-1`},
		{`4 5 10
1 2 1
1 4 1
3 4 1
2 2 100
3 3 100`, `0`},
		{`3 1 10
1 2 20`, `-1`},
	}

	for i, v := range testCases {
		output := utils.Helper(v.input, answer)
		if output != v.expect {
			t.Errorf("case[%d]: expect %v, but %v", i+1, v.expect, output)
		}
	}
}

